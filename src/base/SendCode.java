    final String user = "refangga1952@gmail.com";
    final String pass = "tjygncgtjayunjgl"; 
    String subject = "Kode OTP Reset Password";
    String message = "Kode OTP Anda adalah: " + otpCode;

    Properties props = new Properties();
    props.put("mail.smtp.auth", "true");
    props.put("mail.smtp.starttls.enable", "true"); // Wajib diaktifkan
    props.put("mail.smtp.starttls.required", "true"); // Tambahkan ini
    props.put("mail.smtp.host", "smtp.gmail.com");
    props.put("mail.smtp.port", "587"); // Port untuk TLS
    props.put("mail.smtp.ssl.protocols", "TLSv1.2"); // Tambahkan protocol


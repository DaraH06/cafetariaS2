    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
    String email = txtEmail.getText().trim();

    if (email.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Harap masukkan email terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    if (!isValidEmail(email)) {
        JOptionPane.showMessageDialog(this, "Format email tidak valid!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    randomCode = generateOTP(); // Simpan kode OTP ke variabel global
    boolean emailSent = sendEmail(email, randomCode);

    if (emailSent) {
        JOptionPane.showMessageDialog(this, "OTP telah dikirim ke email Anda.");
        txtVer.setEnabled(true); // Aktifkan input OTP
    } else {
        JOptionPane.showMessageDialog(this, "Gagal mengirim OTP. Silakan coba lagi.", "Error", JOptionPane.ERROR_MESSAGE);
    }
} 
private int generateOTP() {
    Random rand = new Random();
    return 100000 + rand.nextInt(900000);
}

private boolean isValidEmail(String email) {
    return email.matches("^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$");
}

